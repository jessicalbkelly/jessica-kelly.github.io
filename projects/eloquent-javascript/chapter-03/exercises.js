////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(number1, number2) {
  var minimum = "";
  if(number1 > number2) {
    minimum = number2;
  } else {
    minimum = number1;
  }
  return minimum;
}


////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(number) {
  if (number  % 2 === 0) {
    return true;
  } else {
    return false;
  }
}
  

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, letter) {
  var counter = 0;
  for (var i = 0; i < string.length; i++) {
    if (string[i] === letter) {
      counter += 1;
    }
  }
  return counter;
}
  

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  var counter = 0;
  for (var i = 0; i < string.length; i++) {
    if (string[i] === "B") {
      counter += 1;
    }
  }
  return counter;
}

